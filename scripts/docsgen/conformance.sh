#!/usr/bin/env bash
# conformance.sh — generate docs/conformance-status.md from the conformance
# suite. Wraps scripts/conformance-summary.sh (which runs the full suite and
# computes per-suite pass/fail plus in/out-of-scope totals) and embeds its
# output in a fenced block. Output is deterministic (no timestamps); colors are
# auto-disabled because stdout is a pipe, not a terminal. Run via `just docs-gen`.
set -euo pipefail
cd "$(dirname "$0")/../.."

OUT="docs/conformance-status.md"
SUMMARY=$(./scripts/conformance-summary.sh "$@")

{
    echo "<!-- Code generated by scripts/docsgen/conformance.sh; DO NOT EDIT. Regenerate with: just docs-gen -->"
    echo ""
    echo "# Conformance status"
    echo ""
    echo "A snapshot of the rippled-parity conformance suite, produced by"
    echo "[\`scripts/conformance-summary.sh\`](../scripts/conformance-summary.sh). The"
    echo "**in-scope** percentage is the one that matters; out-of-scope suites are"
    echo "intentionally not yet covered — see [conformance.md](conformance.md) and"
    echo "[\`scripts/conformance-out-of-scope.txt\`](../scripts/conformance-out-of-scope.txt)."
    echo ""
    echo "Regenerate with \`just docs-gen\` (runs the full suite)."
    echo ""
    echo '```'
    echo "$SUMMARY"
    echo '```'
} > "$OUT"

echo "wrote $OUT"
