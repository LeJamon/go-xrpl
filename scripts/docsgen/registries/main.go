// Command registries generates docs/supported-transactions.md and
// docs/amendments.md from the live transaction and amendment registries. It
// calls tx/all.RegisterAll() to populate the tx registry with every transaction
// type before enumerating it. Run via `just docs-gen`.
package main

import (
	"fmt"
	"os"
	"sort"
	"strings"

	"github.com/LeJamon/go-xrpl/amendment"
	"github.com/LeJamon/go-xrpl/internal/tx"
	txall "github.com/LeJamon/go-xrpl/internal/tx/all"
)

func writeTransactions() error {
	types := tx.SupportedTypes()
	rows := make([]struct {
		name string
		code uint16
	}, 0, len(types))
	for _, t := range types {
		rows = append(rows, struct {
			name string
			code uint16
		}{t.String(), uint16(t)})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].name < rows[j].name })

	var b strings.Builder
	b.WriteString("<!-- Code generated by scripts/docsgen/registries; DO NOT EDIT. Regenerate with: just docs-gen -->\n\n")
	b.WriteString("# Supported transaction types\n\n")
	b.WriteString("Transaction types registered with the engine, generated from the `tx.Register`\n")
	b.WriteString("registry (`internal/tx`). Each runs the full Validate → Preflight → Preclaim →\n")
	b.WriteString("Apply pipeline. The numeric code is the XRPL `TransactionType` field value.\n\n")
	fmt.Fprintf(&b, "Total: %d transaction types.\n\n", len(rows))
	b.WriteString("| Transaction type | Type code |\n")
	b.WriteString("|------------------|-----------|\n")
	for _, r := range rows {
		fmt.Fprintf(&b, "| `%s` | %d |\n", r.name, r.code)
	}
	return os.WriteFile("docs/supported-transactions.md", []byte(b.String()), 0o644) //nolint:gosec // G306: generated docs artifact, world-readable by intent
}

func yesNo(v bool) string {
	if v {
		return "yes"
	}
	return "no"
}

func writeAmendments() error {
	feats := amendment.AllFeatures()
	sort.Slice(feats, func(i, j int) bool { return feats[i].Name < feats[j].Name })

	var b strings.Builder
	b.WriteString("<!-- Code generated by scripts/docsgen/registries; DO NOT EDIT. Regenerate with: just docs-gen -->\n\n")
	b.WriteString("# Amendments\n\n")
	b.WriteString("XRPL amendments known to this node, generated from the amendment registry\n")
	b.WriteString("(`amendment`). **Supported** means this implementation can apply the\n")
	b.WriteString("amendment's behavior; **Default vote** is whether the node votes for it by\n")
	b.WriteString("default (operators override via the `[amendments]` config section).\n\n")
	fmt.Fprintf(&b, "Total: %d amendments.\n\n", len(feats))
	b.WriteString("| Amendment | Supported | Default vote |\n")
	b.WriteString("|-----------|-----------|--------------|\n")
	for _, f := range feats {
		fmt.Fprintf(&b, "| `%s` | %s | %s |\n", f.Name, yesNo(f.IsSupported()), yesNo(f.IsDefaultYes()))
	}
	return os.WriteFile("docs/amendments.md", []byte(b.String()), 0o644) //nolint:gosec // G306: generated docs artifact, world-readable by intent
}

func main() {
	txall.RegisterAll()
	if err := writeTransactions(); err != nil {
		fmt.Fprintln(os.Stderr, "transactions:", err)
		os.Exit(1)
	}
	if err := writeAmendments(); err != nil {
		fmt.Fprintln(os.Stderr, "amendments:", err)
		os.Exit(1)
	}
	fmt.Println("wrote docs/supported-transactions.md and docs/amendments.md")
}
